valorA = float (input('Digite o valor A: '))
ValorB = float (input('Digite o valor B: '))
ValorC = float (input('Digite o valor C: '))
areaTriangulo = valorA * ValorC / 2
areaCirculo = 3.14159 * ValorC * ValorC
areaTrapezio = (valorA + ValorB) * ValorC / 2
areaQuadrado = ValorB * ValorB
areaRetangulo = valorA * ValorB
print (f'O valor do Triangulo é: {areaTriangulo}, O valor do Circulo é: {areaCirculo}, O valor do Trapézio é: {areaTrapezio}, O valor do quadrado é: {areaQuadrado}, O valor do Retângulo é: {areaRetangulo}.')

